static public void planetPos(int p, out int ra, out int dec) // return planet position. 0 is sun, 1, 2 are mercury, venus, 3-7 are Mars to Neptune
{
    double d=timeFromDate(year, month, day, 0, 0, 0); double sx, sy, sz, sex, sey; 
    sunPos(d, out sx, out sy, out sz, out sex, out sey);
    double ox, oy, oz; objPos(d, p, out ox, out oy, out oz, sex, sey);
    double raf= Math.Atan2(oy, ox)/degToRad/15.0f, decf= Math.Atan2(oz, Math.Sqrt(ox*ox+oy*oy))/degToRad;
    ra= (int)(raf*3600.0f); while (ra<0) ra+= 24*3600;
    dec= (int)(decf*3600.0f);
}

