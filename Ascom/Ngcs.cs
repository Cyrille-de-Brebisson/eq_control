static double timeFromDate(int y, int m, int d, int h, int M, int s)
{
    int D= 367*y - (7*(y+((m+9)/12)))/4 + (275*m)/9 + d - 730530;
    double H= (h+M/60.0f+s/3600.0f)/24.0f;
    return D+H;
}

