// Get sun position compared with earth. unit = AU
static void sunPos(double d, out double sx, out double sy, out double sz, out double ex, out double ey)
{
    double ecl= 23.4393f - 3.563E-7f * d; // ecl angle
    TOrbitalElementsAt sun= new TOrbitalElementsAt(0, d);
    double E= sun.M+sun.e*(180.0f/MPI)*Math.Sin(sun.M*degToRad)*(1.0f+sun.e*Math.Cos(sun.M*degToRad)); //ccentric anomaly E from the mean anomaly M and from the eccentricity e (E and M in degrees):
    // compute the Sun's distance r and its true anomaly v from:
    double xv = Math.Cos(E*degToRad)-sun.e;
    double yv = Math.Sqrt(1.0f-sun.e*sun.e) * Math.Sin(E*degToRad);
    double v = Math.Atan2( yv, xv );
    double rs = Math.Sqrt( xv*xv + yv*yv );
    //compute the Sun's true longitude
    double lonsun = v + sun.w*degToRad;
    // Convert lonsun,r to ecliptic rectangular geocentric coordinates xs,ys:
    ex= sx = rs * Math.Cos(-lonsun);
    ey = -rs * Math.Sin(-lonsun);
    // (since the Sun always is in the ecliptic plane, zs is of course zero). xs,ys is the Sun's position in a coordinate system in the plane of the ecliptic. To convert this to equatorial, rectangular, geocentric coordinates, compute:
    sy= ey * Math.Cos(ecl*degToRad);
    sz= ey * Math.Sin(ecl*degToRad);
}
// get an object position in solar system
static void objPos(double d, int ob, out double x, out double y, out double z)
{
    TOrbitalElementsAt o= new TOrbitalElementsAt(ob, d);
    double E= o.M+o.e*(180.0f/MPI)*Math.Sin(o.M*degToRad)*(1.0f+o.e*Math.Cos(o.M*degToRad)); //ccentric anomaly E from the mean anomaly M and from the eccentricity e (E and M in degrees):
    double E1= E;
    for (int i=0; i<5; i++) E= E - ( E - o.e*(180.0f/MPI) * Math.Sin(E*degToRad) - o.M) / ( 1 - o.e * Math.Cos(E*degToRad) );
    double xv = o.a * ( Math.Cos(E*degToRad) - o.e );
    double yv = o.a * ( Math.Sqrt(1.0f - o.e*o.e) * Math.Sin(E*degToRad) );
    double v = Math.Atan2( yv, xv );
    double r = Math.Sqrt( xv*xv + yv*yv );
    x = r * ( Math.Cos(o.N*degToRad) * Math.Cos(v+o.w*degToRad) - Math.Sin(o.N*degToRad) * Math.Sin(v+o.w*degToRad) * Math.Cos(o.i*degToRad) );
    y = r * ( Math.Sin(o.N*degToRad) * Math.Cos(v+o.w*degToRad) + Math.Cos(o.N*degToRad) * Math.Sin(v+o.w*degToRad) * Math.Cos(o.i*degToRad) );
    z = r * ( Math.Sin(v+o.w*degToRad) * Math.Sin(o.i*degToRad) );
}

static void objPos(double d, int ob, out double x, out double y, out double z, double sex, double sey)
{
    // Sun centered, ecliptic centered coordinates...
    // Need to rotate around earth center by ecliptic!
    // so, add earth coordinate, rotate by ecliptic and stay there...
    double tx, ty, tz;
    objPos(d, ob, out tx, out ty, out tz);
    x= tx+sex; ty= ty+sey;
    double ecl= 23.4393f - 3.563E-7f * d; // ecl angle
    y= ty * Math.Cos(ecl*degToRad) - tz*Math.Sin(ecl*degToRad);
    z= ty * Math.Sin(ecl*degToRad) + tz*Math.Cos(ecl*degToRad);
}

